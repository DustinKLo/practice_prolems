class Solution(object):
    @staticmethod
    def print_map(grid):
        for row in grid:
            print(row)
        print('')

    def maxAreaOfIsland(self, grid):
        """
        :type grid: List[List[int]]
        :rtype: int
        """
        self.print_map(grid)
        self.max_area = 0

        def traverse(y, x):
            # y rows
            # x columns
            if y < 0 or y > len(grid) - 1:
                return
            if x < 0 or x > len(grid[y]) - 1:
                return

            if grid[y][x] == 0:
                return

            self.current_area += 1
            grid[y][x] = 0  # changing to water so we don't revisit this

            traverse(y + 1, x)  # up
            traverse(y - 1, x)  # down
            traverse(y, x - 1)  # left
            traverse(y, x + 1)  # right

        for j in range(len(grid)):  # rows
            for i in range(len(grid[j])):  # columns
                if grid[j][i] == 0:
                    continue
                self.current_area = 0
                traverse(j, i)
                if self.current_area > self.max_area:
                    self.max_area = self.current_area

        print("largest island's area: ", self.max_area)
        print('######################################\n')
        return self.max_area


